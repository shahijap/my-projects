// to check the type of variable we use typeof operator

let a = 2;
console.log(typeof a);

let b = 'shahi'
console.log(typeof b);

let c  = true;
console.log(typeof c);

let d = {
    name: 'shahi',
    age: 23
}
console.log(typeof d);

let e = ['shahi', 'patnam', 23];
console.log(typeof e); // object --> in js arrays are objects so it return type as object